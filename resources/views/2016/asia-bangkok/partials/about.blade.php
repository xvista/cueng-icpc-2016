<h3>About</h3>
<p>
    The <b>ACM ICPC (International Collegiate Programming Contest)</b> is a programming championship held annually under the auspices of ACM (Association for Computing Machinery). The second time the Asia Regional Competition is held in Bangkok by Chulalongkorn University. The Faculty of Engineering and the Department of Computer Engineering are pleased to welcome all teams from universities in Asia. This event will be organized during the 11st and 13rd of November 2016.
